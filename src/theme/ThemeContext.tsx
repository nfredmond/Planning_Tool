export const CustomThemeProvider = ({ children }: { children: React.ReactNode }) => {
  const [mode, setMode] = useState<ThemeMode>('light');

  // Initialize mode from localStorage or system preference
  useEffect(() => {
    const savedMode = localStorage.getItem('themeMode') as ThemeMode | null;
    if (savedMode) {
      setMode(savedMode);
    } else if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
      setMode('dark');
    }
  }, []);

  // Toggle theme function
  const toggleTheme = () => {
    const newMode = mode === 'light' ? 'dark' : 'light';
    setMode(newMode);
    localStorage.setItem('themeMode', newMode);
  };

  // Create the theme
  const theme = useMemo(() => {
    // Get base theme
    const baseTheme = getBaseTheme(mode);
    
    // Get mode-specific theme
    const modeTheme = mode === 'light' ? getLightTheme() : getDarkTheme();
    
    // Merge the themes
    const mergedThemeOptions = deepmerge(baseTheme, modeTheme);
    
    // Create and make the theme responsive
    return responsiveFontSizes(createTheme(mergedThemeOptions));
  }, [mode]);

  return (
    <ThemeContext.Provider value={{ toggleTheme, mode }}>
      <ThemeProvider theme={theme}>
        {children}
      </ThemeProvider>
    </ThemeContext.Provider>
  );
}; 